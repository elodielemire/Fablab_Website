<h3>Qu'est-ce que c'est ?</h3>

Le FabLab est un lieu à la fois de fabrication d'objets et de partage de connaissances. La fabrication par soi-même (DIY) est ici encouragée. Vous pouvez fabriquer presque n'importe quel type d'objet dans un FabLab, grâce essentiellement à des machines contrôlées par ordinateur. Toute personne, quelque soit son niveau de connaissance et ses moyens (nous proposons un accès gratuit), peut venir se servir des machines mises à disposition pour réaliser ses projets qu'ils soient personnels, scolaires, ou commerciaux, et apprendre en faisant soi-même et aussi grâce aux autres. 

<h3>Pour qui ?</h3>

Le FabLab de l'Isen Lille est ouvert à tout type de public : aux architectes pour réaliser des maquettes, aux designers pour réaliser un prototype d'un nouveau modèle, aux entrepreneurs qui souhaiteraient réaliser de petites séries pour tester un marché, aux étudiants pour réaliser un projet pédagogique ou personnel, aux artistes, aux professionnels qui souhaiteraient se fabriquer une enseigne ou des stickers pour leur vitrine, aux chômeurs, aux inventeurs, et à tous ceux qui ont envie de créer quelque chose de palpable.

<h3>Que peut-on faire là-bas ?</h3>

PROTOTYPER<br>
avec une découpeuse laser, une imprimante 3d, de l'outillage pour l'électronique, un atelier classique, et beaucoup d'autres outils encore<br><br>

FABRIQUER<br>
des maquettes et objets sur mesure, unitairement ou en petite série (de "petits" objets, pas des meubles)<br><br>

PERSONNALISER<br>
des objets et réaliser de la signalétique : t-shirt personnalisé par flocage, plaque de Plexi gravée, stickers et pochoirs, lettres découpées en bois, marquage d'objets au laser...<br><br>

APPRENDRE<br>
grâce aux cours proposés pour utiliser par soi même une découpeuse laser, une imprimante 3d, à modéliser en 3d, ...<br><br>

RENCONTRER<br>
des personnes aux compétences diverses :  designers, artistes, ingénieurs, programmeurs, ...<br><br><br><br>
