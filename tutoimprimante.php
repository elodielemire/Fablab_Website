COMMENT CA MARCHE? <br>
La MakerBot Replicator 2 fabrique des objets en volume, tri-dimensionnels à partir de filament 
PLA MakerBot. Vos fichiers de conception 3D sont traduits en instructions compatibles avec la 
MakerBot Replicator 2 et lui sont envoyés par câble USB ou Carte SD. Ensuite, la MakerBot 
Replicator 2 chauffe le filament PLA MakerBot et l'extrude par une buse pour fabriquer, 
couche après couche, un objet en volume. Cette méthode s'appelle la méthode FFF : dépôt de 
filament fondu.

1 Ouvrez le carton <br>
1a. Posez au sol le carton qui contient la MakerBot Replicator 2. Ouvrez-le et retirez la feuille 
de carton mobile . <br>
1b. Sortez le Manuel Utilisateur MakerBot Replicator 2. Nous vous recommandons de vous 
référer au manuel pour installer votre machine et de le garder à portée de main pendant que 
vous déballez ce qu'il y a dans le boite. S'il manque quelque chose par rapport à ce qui est 
décrit dans le manuel, envoyez-nous un courrier électronique à support@makerbot.com. <br>
1c. Retirez la plaque de mousse, ainsi que la feuille de carton juste en dessous. <br>
1d. Retirez les deux pièces protectrices en mousse situées sur les côtés de la Replicator 2. 
REMARQUE: Vous pouvez télécharger une version .pdf de ce manuel à partir de l'adresse 
suivante: makerbot.com/support/replicator2.<br>

2 Sortez la MakerBot Replicator 2 du carton <br>
2a. Ouvrez le plastique et saisissez fermement le cadre de la MakerBot Replicator 2. Sortez 
l’imprimante du carton et posez-la sur une surface stable.<br>
REMARQUE: Le câble noir n'est pas une poignée. Il ne doit, à aucun moment, être tiré ou tordu.
3 Sortez la boite d'accessoires <br>
3a. Retirez les deux pièces protectrices en mousse qui restent au fond du carton de la 
Replicator 2. <br>
3b. Sortez la boite d'accessoires. Cette boite contient les articles mentionnés dans la rubrique 
"Contenu de la boite".<br>
REMARQUE: A présent, votre MakerBot Replicator 2 est entièrement déballée. Nous vous 
recommandons de conserver le carton et les cales en mousse pour transporter ultérieurement 
votre MakerBot Replicator 2.<br><br>

POUR INSTALLER LE PLATEAU D'IMPRESSION <br>
4 Installez le plateau d'impression <br>
4a. Prenez le plateau d'impression et déballez-le. Inclinez le plateau et placez l'encoche sur le 
rebord arrière de la plate-forme de construction. Posez le rebord du plateau d'impression 
entre les deux languettes situées à l'avant de la plate-forme de construction. <br>
4b. Pour retirer le plateau d'impression, poussez doucement les languettes avant vers l'arrière 
de la plate-forme de construction et dégagez le bord du plateau. Soulevez le plateau d'impression.
5 Dégagez la tête d'extrusion <br>
5a. Utilisez de bons ciseaux ou un cutter pour couper les colliers de serrage qui maintiennent le 
système de guidage en place. Jeter les colliers, ainsi que les pièces plastiques qui les 
maintenaient en place. <br>
5b. Une petite pièce en plastique est attachée à la courroie de l'axe X, ainsi qu'aux arbres de 
guidage linéaire. Elle permet de maintenir la buse en place. Déclipsez doucement cette pièce, 
inclinez-la et faites-la glisser pour la dégager de la courroie.<br>
REMARQUE: Manipuler les ciseaux avec précaution.<br>
POUR INSTALLER LE GUIDE-FIL ET LE SUPPORT DE ROULEAU <br><br>
6 Installez le guide-fil pour le filament <br>
6a. Prenez le guide-fil. Insérez une de ses extrémités dans le trou située au dessus de la buse 
et poussez-le aussi loin que possible. <br>
6b. Insérez l'autre extrémité du guide-fil dans le support gauche [vu de l'arrière], à l'arrière de 
la MakerBot Replicator 2. Assurez-vous que l'extrémité du guide-fil est alignée avec le bas du 
support. Le guide-fil ne doit pas dépasser le bas du support de guide-fil.<br>
7 Fixez le support de rouleau <br>
7a. Prenez le support de rouleau. Inclinez-le et fixez-en l'extrémité carrée dans l'emplacement 
de gauche, au dos de la MakerBot Replicator 2 [vu de l'arrière].<br><br>

